const moreDetailBoxes = document.querySelectorAll(".productBox a");
const existingContent = document.querySelector(".mainContentPart");
const bannerText = document.querySelector(".bannerText");
moreDetailBoxes.forEach(link => {
  link.addEventListener("click", (e) => {
    e.preventDefault();

    const productBox = link.closest(".productBox");
    const productName = productBox.querySelector(".cakename").innerText;
    const productType = productBox.querySelector(".cakeType").innerText;

    bannerText.style.display = "none";
    const categoryData = moreDetailData.find(category => category.product === productType);

    const productData = categoryData["ProductInfo"].find(
      item => item.name.toLowerCase() === productName.toLowerCase()
    );


    if (!productData) return;
    if (existingContent) existingContent.remove();

    const currenPageLocation = window.location.pathname.split("/").pop();
    console.log(currenPageLocation)
    if (!document.querySelector(".detailContent")) {
      const newLayoutContent = document.createElement("div");

      newLayoutContent.className = "detaiContent";
      newLayoutContent.innerHTML =
        `
      <div class="reverseIcon">
              <a href="${currenPageLocation}">
                <i class="fa-solid fa-arrow-left"></i>
              </a>
      </div>

     <div class="detailBox">
        <div class="swiper paginationContent">
          <div class="swiper-wrapper">
            ${productData.images.map(img =>
          `
                <div class="swiper-slide">
                  <img src="MoreDetailedProducts/pagination${productType}/${img}" alt="${productName} ">
                </div>
              `
        ).join('')}
          </div>
          <div class="swiper-button-prev">
            <i class="fa-solid fa-arrow-left"></i>
          </div>

          <div class="swiper-button-next">
            <i class="fa-solid fa-arrow-right"></i>
          </div>

          <div class="swiper-pagination"></div>
        </div>

        <div class="productDetailInfo">
            
          <div>
            <h2 class="product-name">${productName}</h2>
            <p class="product-price">
              <span>PRICE</span>
              <span class="price-value">${productData.price.toFixed(2)} AZN</span>
            </p>
            <div class="product-ingredients">
              <h3>INGREDIENTS</h3>
              <p>${productData.ingredients}</p>
            </div>
            <div class="product-size">
              <span>SIZE</span>
              
              ${productData.size.map(s =>
          `
                <span class="size-value">
                 ${s}
                </span>
                `
        ).join('')}
              
            </div>
            <button class="shareBtn">SHARE FACEBOOK</button>
          </div>
        </div>
      </div>
    `
      bannerText.insertAdjacentElement("beforebegin", newLayoutContent)

      new Swiper(".paginationContent", {
        loop: true,
        speed: 800,
        navigation: {
          nextEl: ".swiper-button-next",
          prevEl: ".swiper-button-prev",
        },
        pagination: {
          el: ".swiper-pagination",
          clickable: true,
        }
      });
    }
  })
})

